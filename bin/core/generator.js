class RuleGenerator {
    /**
     * Generate content for AGENTS.md, ling.rules and codex.json
     * @param {object} transformResult Result from Transformer
     * @returns {object} { agentsMd: string, lingRules: string, codexJson: object }
     */
    static generate(transformResult, version) {
        if (typeof version !== "string" || version.trim() === "") {
            throw new Error("RuleGenerator.generate 需要显式传入版本号");
        }
        const { metadata } = transformResult;
        
        // 1. Generate codex.json
        const codexJson = {
            version: version,
            updatedAt: new Date().toISOString(),
            skills: metadata.map(m => ({
                id: m.id,
                originalName: m.originalName,
                path: m.path
            }))
        };

        // 2. Generate AGENTS.md managed section content
        let agentsMd = `# AI Agent Capabilities (Codex Managed)\n\n`;
        agentsMd += `> **Auto-generated by Ling (Codex)**\n`;
        agentsMd += `> Version: ${version}\n\n`;
        
        agentsMd += `## Available Skills\n\n`;
        
        for (const meta of metadata) {
            agentsMd += `- **${meta.id}** (${meta.type})\n`;
            agentsMd += `  - Path: \`${meta.path}\`\n`;
        }
        
        agentsMd += `\n## Usage Rules\n\n`;
        agentsMd += `1. Managed resources are synchronized under \`.agents/skills\`.\n`;
        agentsMd += `2. Do not rename managed skill folders manually.\n`;
        agentsMd += `3. Use \`ling doctor --target codex --fix\` to recover missing managed artifacts.\n`;
        agentsMd += `4. If \`issues.csv\` exists, treat it as the task tracking source of truth and keep at most one task in \`进行中\`.\n`;

        // 3. Generate risk controls
        let lingRules = `# Ling Risk Controls (Codex Managed)\n\n`;
        lingRules += `version: ${version}\n`;
        lingRules += `generated_at: ${new Date().toISOString()}\n\n`;
        lingRules += `## Controls\n`;
        lingRules += `- Managed skills are stored under \`.agents/skills\`.\n`;
        lingRules += `- Keep generated skill IDs stable to avoid selector drift.\n`;
        lingRules += `- If integrity checks fail, run \`ling doctor --target codex --fix\`.\n`;
        lingRules += `- Before destructive updates, preserve manual edits in source control.\n`;

        return {
            agentsMd,
            lingRules,
            codexJson
        };
    }
}

module.exports = RuleGenerator;
